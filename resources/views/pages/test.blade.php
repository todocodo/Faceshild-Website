    <h3 style="padding-left: 75pt;text-indent: 0pt;text-align: center;">INSTRUCTION AND INFORMATION ABOUT THE MANUFACTURER
    </h3>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-top: 7pt;padding-left: 34pt;text-indent: 0pt;line-height: 131%;text-align: left;">nTr Professional Faceshield <span class="p">- consists of extremely lightweight and elegant frame in which through a
            click system a reusable screen is installed. The frame is placed on the head with the help of an eraser that
            can be adjusted individually. For additional comfort a mellow protector for the forehead is placed. The set
            consists of:</span></h3>
    <p style="padding-left: 34pt;text-indent: 35pt;line-height: 131%;text-align: left;">Frame - lp. made of PP with protector of EVA and eraser with width 10.5mm</p>
    <p style="padding-left: 69pt;text-indent: 0pt;line-height: 131%;text-align: left;">Front Protective Window - Sp. that are 0.2mm thick and made of PET Instruction and Information for the Producer - lp.</p>
    <p style="padding-left: 69pt;text-indent: 0pt;line-height: 13pt;text-align: left;">Box- lp.</p>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-left: 34pt;text-indent: 0pt;line-height: 131%;text-align: left;">nTr Professional Faceshield
        <span class="p">- protects from drops and splashes of liquids, responding to the European Regulation (EC)
            2016/425, European Standard EN166:2001 point 7.2.4 b, EN168:2008 objection 12.2 and the requirements for
            EN166:2001 point 6.1, 6.2, 6.3;</span></h3>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-left: 34pt;text-indent: 0pt;line-height: 131%;text-align: left;">nTr Professional Faceshield
        <span class="p">- can be used with mask on as well as with protective or optical glasses.</span></h3>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-left: 34pt;text-indent: 0pt;line-height: 131%;text-align: left;">nTr Professional Faceshield
        <span class="p">- is incredibly lightweight - only 46 g. it does not disturb the eye vision or the free movement
            of the head. It can be worn for hours without fatigue.</span></h3>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-left: 34pt;text-indent: 0pt;line-height: 131%;text-align: left;">nTr Professional Faceshield
        <span class="p">- is not disinfected. Before usage disinfect the faceshield. Suitable for disinfection are all
            disinfectors containing ethanol. Avoid solvents, ketones, strong acids and alkaline elements.</span></h3>
    <p style="text-indent: 0pt;text-align: left;"><br /></p>
    <h3 style="padding-left: 34pt;text-indent: 0pt;text-align: left;">nTr Professional Faceshield <span class="p">-
            store in a dry place with temperature from -25C to</span></h3>
    <p style="padding-top: 4pt;padding-left: 33pt;text-indent: 0pt;text-align: left;">+SOC in its box for no longer than 5 years.</p>
